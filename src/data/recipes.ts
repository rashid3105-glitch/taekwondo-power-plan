export function getRecipes(locale: string = "en"): Recipe[] {
  // NO falls back to DA, SV uses its own block, DE/AR fall back to EN.
  const lang: "en" | "da" | "no" | "sv" =
    locale === "da" ? "da" :
    locale === "no" ? "no" :
    locale === "sv" ? "sv" :
    "en";
  return recipesData.map((r) => {
    const text = r[lang] ?? r.en;
    return {
      id: r.id,
      category: r.category,
      calories: r.calories,
      protein: r.protein,
      carbs: r.carbs,
      fat: r.fat,
      name: text.name,
      prepTime: text.prepTime,
      ingredients: text.ingredients,
      steps: text.steps,
      tip: text.tip,
    };
  });
}

